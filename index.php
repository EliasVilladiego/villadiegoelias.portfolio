            <h2 class="center"> ¿Qué es la Casa del Pumarejo? </h2>
            <br />
            <p> Es una Casa Palacio del siglo XVIII en el casco histórico de Sevilla convertida en casa de vecindad
                a raíz de sufrir un intento de explusión de sus vecinas y vecinos por parte de una empresa hotelera
                en el año 2000. En la actualidad ya es un edificio propiedad del Ayuntamiento, protegido como Bien
                de Interés Cultural por la Consejería de Cultura de la Junta de Andalucía y autogestionado por los
                vecinos del barrio a través de la Asociación Casa del Pumarejo.
                <br />
                Esta asociación está conformada por una diversidad de personas y colectivos que promueven una
                multitud de actividades: culturales, artísticas, políticas, sociales y artesanales. Actividades de
                lucha, fiesta, conocimiento, ayuda mutua y arte que nos aportan vivencias que construyen una manera
                de seguir en un proceso permanente.
            </p>
            <br />
            <h2 class="center"> ¿Quienes conforman la Casa? </h2>
            <br />
            <p> La Asociación Casa del Pumarejo la conformamos quienes habitamos la Casa Grande: las vecinas que
                residen, los comercios y las entidades y colectivos sociales que, con nuestras actividades
                cotidianas desarrolladas en el Centro Vecinal Pumarejo «Felisa García» (abierto en 2004 y cuyas
                dependencias gestiona la Asociación por cesión administrativa desde 2011), damos vida y defendemos
                este emblemático y valioso edificio como espacio de encuentro, cultura, trabajo ciudadano y cohesión
                barrial.
                <br />
                Actualmente conformamos este tejido asociativo -y afectivo- una veintena de entidades sociales y
                culturales que día a día trabajamos por un barrio y una Sevilla mejores.
            </p>


