            <h2 class="center">Origen</h2><br>
            <p class="center">En el año 2000, ante la compra de la mitad de la Casa Palacio de Pumarejo para un Hotel de
                lujo y el intento de expulsión de 12 vecinas y 10 comerciantes de la Casa, se crea la Plataforma en
                Defensa de la misma. En esta época, el barrio estaba padeciendo una política de transformación
                urbanística a partir del Plan Urban (1994-1999), que eran fondos europeos para la rehabilitación de
                zonas degradadas. Este proyecto provocó la ruina forzada de edificios históricos, la expulsión del
                vecindario tradicional y la especulación.</p><br>
            <br>
            <h2 class="center">Movilización</h2><br>
            <p class="center">Esta venta del Palacio provocó una movilización de las vecinas y entidades barriales, de
                defensa del patrimonio, etc …, que participaban dentro de la Plataforma por la Casa del Pumarejo. La
                acción de ésta se centró en la defensa del edificio, de las familias y el mantenimiento de la variedad
                de usos. La diversidad de acciones culturales, de denuncias, jornadas, investigaciones, etc…, dio a
                conocer esta problemática y se consiguió que la Casa de Pumarejo fuese inscrita en el 2003 en el
                Catálogo General del Patrimonio Histórico de Andalucía con la categoría de MONUMENTO. Su declaración
                como Bién de Interés Cultural (B.I.C.), establece la protección tanto de lo arquitectónico como de su
                valor etnológico bloqueando así su privatización. En el 2009, la Casa de Pumarejo se convierte en un
                «equipamiento público» y el Ayuntamiento compra el edificio. Siendo en el 2011 cuando la Asociación Casa
                de Pumarejo firma un Convenio por 15 años.</p><br>
            <br>
            <h2 class="center">Espacios</h2><br>
            <p class="center">Tras esta declaración valorizando la diversidad de usos y la necesidad de reutilizar
                espacios para realizar actividades e impulsar otras nuevas; el colectivo de la Casa decidió rehabilitar
                vecinalmente algunos espacios abandonados del edificio. Así en 2004, se abrió un espacio de encuentro y
                trabajo ciudadano en la planta baja del edificio llamado “Centro Vecinal”. Dicho espacio de uso y
                gestión comunitaria, permitió acoger y promover infinidad de actividades y actos: reuniones de
                asociaciones y colectivos, charlas, mesas redondas, exposiciones, talleres, cursos, encuentros
                vecinales, fiestas… Más tarde, con la idea de seguir revitalizando la Casa y para recibir más grupos, la
                Asociación decidió abrir dos dependencias: El “Bajo 5” en el año 2005 y el espacio «Rosa Moreno» en el
                2009 (compartido por la Biblio-Puma y el Mercadillo Cultural Pumarejo).</p><br>
            <br>
            <h2 class="center">Organización</h2><br>
            <p class="center">Todos estos espacios están organizados a través de una Asamblea mensual donde se trata de
                la globalidad de la Casa, seguimiento de las actividades planteadas, las negociaciones con la
                administración, etc. Con el aumento de las actividades y la incapacidad por parte de la Asamblea de
                resolver todas las problemáticas, se han ido creado varias Comisiones de Trabajo más especificas
                (Comisión Casa, Equipo de Acogida, Equipo de Mantenimiento y Equipo de Comunicación). En el camino,
                hemos aprendido que las Comisiones aparecen y desaparecen como si tuvieran un ciclo vital, simplemente,
                se agotan por sí mismas. Las estructuras son tratadas como herramientas para la comunidad.</p><br>
            <br>
            <h2 class="center">Acontecimientos</h2><br>
            <p class="center">Durante quince años la tónica de las relaciones con las Administraciones: Junta de
                Andalucía y Ayuntamiento, nos han desgastado mucho. Hemos pasado por el gobierno de los cuatro partidos
                existentes en la ciudad (Partido Andalucista, Partido Socialista, Izquierda Unida y el Partido Popular).
                Ninguno de ellos nos han comprendido, todo ha sido una constante lucha para conseguir reuniones, para
                llegar a mínimos acuerdos o trabajos conjuntos. Este contínuo trato de desconsideración nos llevó a dar
                un salto a instituciones de ámbito mayor y también del extranjero. Y, por último, hemos realizado Obras
                de Mantenimiento y Conservación en la Casa ante la no respuesta a las solicitudes de permiso de obras.
                Tras donaciones de la ciudadanía, hemos conseguido los fondos necesarios. Una vez finalizada la tarea,
                organizamos “La Fiesta del Sambombazo” para mostrar a la población, a todos los partidos, a las
                instituciones que no se habían dignado a responder, etc, cómo había quedado la Casa tras el arreglo.
                Esta actitud que podemos resumir como de abandono de los deberes de Conservación, Mantenimiento, Tutela
                y Difusión del Palacio de Pumarejo, nos ha llevado a considerar dicha actitud como un Expolio
                Institucional que impide que este Bien alcance su finalidad, o sea, ser un beneficio para la ciudad y
                sus habitantes.</p><br>
            <br>
            <h2 class="center">Relaciones</h2><br>
            <p class="center">Nuestro parto fue dentro de un barrio marcado por su Historia, es decir, sus conflictos
                sociales, su fuerte amor por sus gentes, sus formas de vivir … las cuales nos han ido modificando,
                madurando, recreando… A la vez, fuimos estudiando cómo se había originado este Palacio, su plaza, sus
                conflictos y disfrutes que en su Historia han convivido… Estos saberes, los vamos actualizando al entrar
                en contacto con otros vecinos recién llegados. Descubrimos un ser vivo que se autoprotege, que quiere
                seguir vivo tal y como se hace a sí mismo; descubrimos la realidad de su todavía existente sentimiento
                de barrio que se reconstruye una y otra vez con el paso del tiempo, de sus habitantes… Los años nos
                pusieron delante de otra realidad: asociaciones y entidades de la ciudad nos llamaban para participar en
                sus actividades, charlas, conferencias… Al principio, no nos dimos cuenta de la relevancia de que
                personas ajenas al barrio también se sintieran parte del cuidado de esta Casa. Vimos que “vecino” no es
                sólo el que reside en la Casa Palacio, sino el que se siente parte de Ella y aporta desde sus
                habilidades y ámbitos. No sólo visitamos otros espacios sino que nos abrimos a la llegada de otras
                concepciones, como el mundo del “Decrecimiento”… que trajo posteriormente la creación de la Moneda
                Social el Puma…. que hoy cuenta con 800 miembros y que ha permitido «re-tejer» tiendas y asociaciones.
                La Casa se ha convertido al igual que su plaza en un cruce de movimientos, concepciones… donde conviven
                defensores de la Vivienda, del Patrimonio, de la Participación Ciudadana, de la Creación Artística, de
                la Generación de Conocimientos, de la Economía Solidaria… en definitiva, otro marco político que
                devuelva a la vida social a tantos y tantos expulsados del “Sistema Máquina” actual. Como una onda en el
                agua que se expande amistosamente hacia otros espacios en Andalucía (link casa invisible…); casi sin
                darnos cuenta nos vimos convocados por otras ciudades y espacios del estado Español (Vitoria, Zaragoza,
                Barcelona… ) y desde aquí, el salto al extranjero… Y es real, hay miles de relaciones que no vemos en
                nuestros pequeños pasos, en nuestros gestos de apertura que van más allá de lo que incluso deseamos, así
                llegaron invitaciones desde Australia, Italia, Francia…, que nos traen nuevas ideas como la de iniciar
                una Red de Espacios Ciudadanos para compartir y ampliar nuestra potencia colectiva.</p><br>
            <br>
            <h2 class="center">Valores</h2><br>
            <p class="center">Para los vecinos de la Casa Grande del Pumarejo, las palabras cargadas de valores son sus
                únicas armas, en un contexto de construcción y fusión incesante entre acción y reflexión. Esta inercia
                brota de la injusticia, de la especulación y del intento de destrucción de la cultura de un barrio, de
                un sistema económico en declive, del desencanto político, del inconformismo, e incluso del propio
                lenguaje preso de una realidad alienante,… Surgen nuevos valores y significados. El concepto de
                “vecino/vecina” se expande: se compromete con la cultura del barrio, se integra en una comunidad
                solidaria de afectos y de cuidados, que vela por el “común”, independientemente de su lugar de
                residencia. O la Moneda Social Puma que pone de manifiesto la riqueza de este colectivo basada en tres
                ideas nucleares: “comunidad”, “confianza” y “abundancia”. Sin saberlo estos valores han trascendido
                nuestras fronteras y ha surgido la necesidad natural de compartirlos y de evaluar, estudiar y asimilar
                aportaciones del exterior como por ejemplo el “sentir-pensar”. Es la belleza de un eco-sistema en
                constante movimiento y en sintonía con los vecinos, con la comunidad y con la naturaleza.</p><br>
            <p class="center">Sevilla es una ciudad mediterránea, de tamaño medio (unos 700, 000 habitantes). Andalucía
                está al sur del estado español, la ciudad se ha ido construyendo, como tantas otras, en el transcurso de
                su propia historia. Tener su calzada romana, tener restos de la muralla árabe, tener mezquitas
                convertidas en iglesias… tener el recuerdo del descubrimiento de las Américas, en edificios… en la
                memoría de toda una ciudad que se ha ido haciendo de ilusiones y desiluciones, alegrías y penas… como
                todo ser vivo. Es la vida de un pequeño barrio, San Julian, que se mueve como un hormiguero. Tiene un
                corazón que es la plaza y una Casa Palacio que se construyó en el año 1775, precisamente por un
                “indiano” (persona pobre que se hace rica con el comercio en las Américas) que se hizo rico con el
                negocio de las maderas, e hizo derribar 70 viviendas para generar la plaza de Pumarejo. (Enlace
                historia)</p><br>
            <p class="center">Este palacio llegó a convertirse en vivienda de más de 60 familias a partir del año 1930.
                Con la idea del desarrollismo y la modernidad el Plan Urban 1994- 1999 con fondos europeos se pretende
                acondicionar la zona y atraer a nuevos vecinos al barrio. Es en el año 2000 cuándo una serie de vecinas
                de la Casa reciben la noticia de que un nuevo propietario desea hacer un Hotel, les transmite y les
                presiona con los “asustaviejas”; han de marcharse.. Ellas se niegan y comienzan a reunirse con personas
                de su barrio a traves de hijos, amigos, vecinos del entorno, comerciantes… y se forma la Plataforma por
                la Casa del Pumarejo.</p><br>
            <br>
            <h2 class="center">2000</h2><br>
            <p class="center">Las tres ideas nucleares de la lucha fueron: reutilizar, rehabilitar y revitalizar. Erre
                que erre. Estas personas mayores se abrieron a todo tipo de personas: artistas del barrio,
                patrimonialistas, movimientos políticos, ocupas, profesores de la universidad, profesionales… donde cada
                uno aportaba sus saberes y habilidades para entre todos impedir el desalojo de estas vecinas y la
                pérdida de los valores culturales, de convivencia… o contra los poderosos y sus políticas de
                expeculación…</p><br>
            <br>
            <h2 class="center">2003</h2><br>
            <p class="center">La primera iniciativa de la Plataforma es la realización de un estudio de la historia de
                la Casa para conseguir la declaración de Bien de Interés Cultural (enlace) lo que trastocó la diversidad
                de aportaciones que cada mirada incorporaba. El conocimiento de la historia y del contexto permite otra
                lectura de lo que está sucediendo y convierte a los lectores pasivos en “actores activos” de su propia
                historia. Empezamos a construir un nuevo vocabulario. No nos encontrabamos cómodos con los significados
                antíguos y se pusieron a pulir aquello que les hacía sentirse mal o incomprendidos.</p><br>
            <p class="center">Se dieron cuenta que el concepto de “vecino” se tenía que transformar. Existe tanta
                diversidad en el interior de la Plataforma que no se puede aceptar una visión clásica de vecino donde
                los jóvenes que están llegando después de 10 o 15 años todavía no se les considera del barrio. Es una
                mezcla no prevista que comienza a tomar un “contenido” propio. Tomando lo que de positivo tiene cada
                persona sea jóven o anciana, nacida o no en el barrio… todos se relacionan y conviven por un “bien”, aún
                desconocido, en construcción, pero que sí tenían muy claro ante qué se oponían: la desaparición de un
                ser vivo; la cultura de un barrio, por un fenómeno de especulación.</p><br>
            <br>
            <h2 class="center">2005</h2><br>
            <p class="center">Rocio Pozo lo explicaba así: “Y los lazos creados han ido creciendo entre los miembros de
                la Plataforma como los jaramagos en la azotea, sin planificación ninguna. El sentimiento de comunidad
                afectiva es imprescindible tenerlo en cuenta a la hora de analizar las interrelaciones sociales … de la
                Plataforma.” Lo relevante tiene que ver con la creación de una comunidad de afectos. Fueron otros
                vecinos del barrio los que se acercaron a la Casa Palacio para transmitir los conflictos en la plaza por
                la presencia de personas sin vivienda, con problemas de drogadicción… o por los desahucios y expulsiones
                de comerciantes, artesanos en distintas casas, locales y corralones… La Casa pasó a ser referente de
                otras personas, barrios… y se convirtió en un espacio para nuevos modelos de acción.</p><br>
            <br>
            <h2 class="center">2009</h2><br>
            <p class="center">Se debilitó la lucha. Se amainaron los movimientos sociales. Se agotó el ánimo para ir una
                y otra vez a las administraciones, partidos políticos… No quisimos responder a las provocaciones que nos
                llegaban desde las administraciones. Volvimos a los encuentros, a debates entre nosotros y abiertos a la
                sociedad… y nos dimos cuenta que el debate entre lo público y lo privado era falso, ambos nos atacaban y
                brotó LO COMÚN.</p><br>
            <br>
            <h2 class="center">2010</h2><br>
            <p class="center">Un año antes del 15M llegaron y atrajimos nuevos aires que se manifestaron en la entrada
                con fuerza de movimientos como Decrecimiento y la PAH entre otros. Eran personas jóvenes… con otras
                ideas y valores. Coincidió con conflictos internos no resueltos dentro de la Asociación Casa del
                Pumarejo como la idea de no poder debatir sobre “Economía”. Todos los esfuerzos de los primeros nueve
                años habían sido “sin ánimo de lucro”, todo era desinteresado, éramos “altruistas”, primero como opción
                y después por “imposición”… todo lo demás nos recordaba que “el dinero todo lo corrompe”… pero la gente
                que defendía y construía la Casa se tenía que marchar porque no tenía empleo, ni los medios mínimos de
                subsistencia…. “Lo común” se desarticulaba si sólo pensábamos en la Casa, también hay que pensar en las
                personas que la habitan y dan vida. Es un todo que forma una unidad.</p><br>
            <br>
            <h2 class="center">2012</h2><br>
            <p class="center">En este año se originó la Moneda Social el Puma con personas de la Casa y de
                Decrecimiento. Esto fue una nueva fase de creación de valores que iban surgiendo según la necesidad y lo
                que los acontencimientos nos iban poniendo por delante. Se llegó a definir la Moneda no como un
                sustituto del euro sino como una herramienta para sacar a la luz nuestras riquezas que eran: Comunidad,
                Confianza y Abundancia. Ha sido un debate entre dos conceptos nuevos: El sustento y los cuidados.
                Surgieron por separado y se han ido integrando uno dentro del otro. Hay quienes optan con su vida como
                un medio de aprendizaje, salirse de lo dado… a pecho abierto, con su sangre, con sus pasiones, con su
                negativa a la esclavitud abren, agrietan… y brotan nuevas vías. No se sienten ni quieren ser mártires,
                ni militantes, ni iluminados, ni expertos, …, simplemente, reconocer su propia naturaleza y el
                ecosistema donde les ha tocado habitar.</p><br>
            <p class="center">En estas confluencias surgen dolores, rupturas, desequilibrios… no es solo cuestión de
                buena o mala fe, es cuestión de conocimiento, de apertura a la totalidad, de la diversidad de
                naturalezas, de carácter, de capacidad de convivencia con la incertidumbre… No sólo era necesario el
                afecto sino una capacidad de comprensión de la realidad que nos supera en el estricto ámbito en el que
                nos han ubicado o nos hemos acomodado.</p><br>
            <p class="center">Sólo la realidad nos despierta, sólo nuestra naturaleza salta cuándo no le dejan ser o
                desarrollarse, no proviene de un discurso, no proviene de una ideología, de una religión, de una
                ciencia… proviene de un choque vital, que cada uno ha de afrontar o subordinarse a lo dado. Quizás
                seamos un grupo de artistas de lo cultural – político que no nos atrae lo existente; el empleo, no nos
                atrae el dinero, no nos atraen los intereses, no nos atrae el individualismo, … nos atrae algo más
                sutil, que vuela en el aire como las semillas, como los días de primavera que nos envuelve de luz,
                calor, olor… que nos despiertan cada día a la vida.</p><br>
            <p class="center">Quizás estemos fabricando tiempo para que tu, lector, te unas al proceso. No lo podemos
                hacer solos, es más, sabemos que no sería “bueno”: sano, útil, correcto…. No podemos hacer lo mismo que
                criticamos: correr, acelerarnos, enfadarnos con los demás, exigirnos, hasta dañar nuestra naturaleza.
                Quizás seamos las preguntas que aún nadie consigue pronunciar y que la vida ayudará a responder. No
                sabemos como es, pero sabemos que existe, que ha existido, que está delante nuestra y no la vemos por
                una realidad virtual que la tapa. Es un globo que sólo se pincha con nuestra presencia, con nuestra
                consciencia, con un hacer que saca a la luz la distinción entre realidad y apariencia. Quizás seamos un
                otro “hacer” cargado de arte, que promoves otra época, una nueva política donde se superan las medidas,
                las contabilidades… son acontecimientos que dicen que “ya ha sucedido”; Hemos arreglado el patio de la
                Casa Palacio ante la no respuesta de las administraciones a nuestra petición de permiso de obras.</p>
            <br>
            <p class="center">El reloj se vuelve a poner en marcha. Una época ha comenzado a degradarse (el capitalismo
                consumista), y ya comienza otra; no le podemos responder con sus palabras, conceptos, herramientas,
                normas, leyes, sistemas… ellos son los que han originado el problema y necesitamos nuevas herramientas.
                Para nosotros las armas son las palabras cargadas de valores, de fusión entre acción y reflexión.
                Todavía estamos fabricando las palabras, por ello, no nos gustan los discursos, no nos sentimos
                preparados, maduros, ni con la diversidad suficiente en nuestro interior para que abarque a la totalidad
                de la que quisieron expulsarnos. Lo que hicimos para no salir de la Casa es para el barrio, para la
                ciudad y para la Humanidad. Ahora estamos en el “sentir-pensar”.</p><br>
            <p class="center">Que no sepa el Sistema Máquina si somos partículas u ondas. La vida tiende a seguir.
                Estamos, y se están ya, redactando muchas otras lecturas de lo vivido…</p><br>
